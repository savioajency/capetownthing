	/**
	 * @return array
	 */
	public static function getList(){
		$icons  = self::getIcons();
		$result = [
			'success' => false
		];
		if(isset($_GET['id']) AND isset($icons[$_GET['id']])){
			$result['success'] = true;
			$result['data'] = $icons[sanitize_text_field($_GET['id'])];
			return $result;
		}

		$list = [];
		foreach ($icons as $key => $icon)
			$list[$key] = $icon['name'];

		$result['success'] = true;
		$result['data'] = $list;

		return $result;
	}
